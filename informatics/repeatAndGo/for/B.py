# Задача №3542. Диофантово уравнение - 1
# Даны числа a, b, c, d. Выведите в порядке возрастания все целые числа от 0 до 1000, которые являются корнями
# уравнения ax3+bx2+cx+d=0.
#
# Входные данные
# Вводятся целые числа a, b, c и d.
#
# Выходные данные
# Выведите ответ на задачу. Если в указанном промежутке нет корней уравнения, то ничего выводить не нужно.

def main():
    a, b, c, d = [int(int(input())) for x in range(4)]
    for x in range(1001):
        r = a * x**3 + b * x**2 + c * x + d
        if r == 0:
            print(x)


if __name__ == '__main__':
    main()
